package Practice;

public class Ittehad {
    private int a;
    private int b;
    private int c;
    private int tor_nani;
    private int noki_khanki;

    public Ittehad(int a, int b, int c, int tor_nani, int noki_khanki) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.tor_nani = tor_nani;
        this.noki_khanki = noki_khanki;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }

    public int getTor_nani() {
        return tor_nani;
    }

    public void setTor_nani(int tor_nani) {
        this.tor_nani = tor_nani;
    }

    public int getNoki_khanki() {
        return noki_khanki;
    }

    public void setNoki_khanki(int noki_khanki) {
        this.noki_khanki = noki_khanki;
    }
}
